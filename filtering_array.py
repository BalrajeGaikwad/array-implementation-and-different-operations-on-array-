#10. Filling an Array with Specific Values
#You can create arrays filled with specific values.


import numpy as np

zeros_array=np.zeros((3,3))

ones_array=np.ones((3,3))

constatnt_array=np.full((3,3),7)

print("Array filled with zeroes:")
print(zeros_array)

print("Array filled with ones:")
print(ones_array)

print("Array filled with constant array :")
print(constatnt_array)